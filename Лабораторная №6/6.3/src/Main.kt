fun main() {
    // Создаем массив из 10 целых чисел
    val numbers = arrayOf(15, 3, 22, 8, 42, 7, 19, 5, 31, 12)

    // Инициализируем переменные для хранения максимального и минимального значений
    var max = numbers[0]
    var min = numbers[0]

    // Проходим по всем элементам массива
    for (number in numbers) {
        if (number > max) {
            max = number // Обновляем максимальное значение
        }
        if (number < min) {
            min = number // Обновляем минимальное значение
        }
    }

    // Выводим результаты
    println("Максимальное значение: $max")
    println("Минимальное значение: $min")
}
