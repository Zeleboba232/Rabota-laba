fun main() {
    // Создаем массив из целых чисел
    val numbers = arrayOf(1, 2, 3, 4, 5)

    // Инициализируем переменную для хранения суммы
    var sum = 0

    // Суммируем элементы массива
    for (number in numbers) {
        sum += number
    }

    // Выводим результат
    println("Сумма элементов массива: $sum")
}
